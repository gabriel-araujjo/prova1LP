#ifndef QUESTAO_1_H_
#define QUESTAO_1_H_

/**
 * Calcula a soma dos n primeiros elementos de progressão geometrica de
 * fator 1/2
 *
 * @param n - índice do último elemento a ser somado
 * @return soma dos n primeiros elementos de uma progressao geométrica
 *          de fator 1/2
 *
 * Implementação recursiva
 */
double soma_sequencia_r(int n);

/**
 * Calcula a soma dos n primeiros elementos de progressão geometrica de
 * fator 1/2
 *
 * @param n - índice do último elemento a ser somado
 * @return soma dos n primeiros elementos de uma progressao geométrica
 *          de fator 1/2
 *
 * Implementação iterativa
 */
double soma_sequencia_i(int n);

#endif // QUESTAO_1_H_
